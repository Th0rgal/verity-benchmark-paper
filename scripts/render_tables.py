#!/usr/bin/env python3
import argparse
import json
from collections import Counter, defaultdict
from pathlib import Path


def tex_escape(value):
    text = str(value)
    replacements = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
        "~": r"\textasciitilde{}",
        "^": r"\textasciicircum{}",
    }
    return "".join(replacements.get(ch, ch) for ch in text)


def pct(part, total):
    return 0 if not total else 100 * part / total


def pct_text(part, total):
    return f"{pct(part, total):.1f}\\%"


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--benchmark-dir", required=True)
    parser.add_argument("--out-dir", required=True)
    args = parser.parse_args()

    bench = Path(args.benchmark_dir)
    out = Path(args.out_dir)
    out.mkdir(parents=True, exist_ok=True)

    version = json.loads((bench / "benchmark-versions" / "v0.1.json").read_text())
    summary = json.loads((bench / "results" / "summaries" / "v0.1.json").read_text())

    tasks = version["tasks"]
    cases = defaultdict(list)
    for task in tasks:
        cases[task["case_id"]].append(task)

    difficulties = Counter(task["difficulty"] for task in tasks)
    proof_families = Counter(task["proof_family"] for task in tasks)
    property_classes = Counter(task["property_class"] for task in tasks)

    complete_rows = [row for row in summary["models"] if row.get("status") == "complete"]
    complete_rows.sort(key=lambda row: (row["passed"], row["task_count"]), reverse=True)

    noncomplete_rows = [
        row for row in summary["models"] if row.get("status") in {"partial", "invalid"}
    ]
    noncomplete_rows.sort(
        key=lambda row: (
            0 if row.get("status") == "partial" else 1,
            -row["passed"],
            -row["task_count"],
        )
    )

    macros = []
    macros.append("% Generated by scripts/render_tables.py; do not edit manually.\n")
    macros.append(r"\newcommand{\BenchmarkVersion}{0.1}" + "\n")
    macros.append(r"\newcommand{\BenchmarkTaskCount}{" + str(len(tasks)) + "}\n")
    macros.append(r"\newcommand{\BenchmarkCaseCount}{" + str(len(cases)) + "}\n")
    macros.append(r"\newcommand{\BenchmarkFamilyCount}{" + str(len(set(t["family_id"] for t in tasks))) + "}\n")
    macros.append(r"\newcommand{\BenchmarkTaskSetId}{\texttt{" + tex_escape(version["task_set_id"].split(":", 1)[1][:12]) + "}}\n")
    macros.append(r"\newcommand{\BenchmarkHarnessId}{\texttt{" + tex_escape(version["harness_id"].split(":", 1)[1][:12]) + "}}\n")
    macros.append(r"\newcommand{\BenchmarkEnvironmentId}{\texttt{" + tex_escape(version["environment_id"].split(":", 1)[1][:12]) + "}}\n")
    macros.append(r"\newcommand{\BenchmarkGitSha}{\texttt{" + tex_escape(version["git_sha"][:12]) + "}}\n")
    macros.append(r"\newcommand{\BenchmarkEasyTasks}{" + str(difficulties.get("easy", 0)) + "}\n")
    macros.append(r"\newcommand{\BenchmarkMediumTasks}{" + str(difficulties.get("medium", 0)) + "}\n")
    macros.append(r"\newcommand{\BenchmarkHardTasks}{" + str(difficulties.get("hard", 0)) + "}\n")
    macros.append(r"\newcommand{\BenchmarkOtherDifficultyTasks}{" + str(sum(v for k, v in difficulties.items() if k not in {"easy", "medium", "hard"})) + "}\n")

    lines = []
    lines.append("% Generated by scripts/render_tables.py; do not edit manually.\n")
    lines.append(r"\begin{table*}[t]" + "\n")
    lines.append(r"\caption{Benchmark suite composition in v0.1.}" + "\n")
    lines.append(r"\label{tab:suite}" + "\n")
    lines.append(r"\centering" + "\n")
    lines.append(r"\footnotesize" + "\n")
    lines.append(r"\begin{tabular}{lrrl}" + "\n")
    lines.append(r"\toprule" + "\n")
    lines.append(r"Case & Tasks & Hard & Dominant property \\" + "\n")
    lines.append(r"\midrule" + "\n")
    for case, case_tasks in sorted(cases.items()):
        props = Counter(t["property_class"] for t in case_tasks)
        hard = sum(1 for t in case_tasks if t["difficulty"] == "hard")
        lines.append(
            f"{tex_escape(case)} & {len(case_tasks)} & {hard} & {tex_escape(props.most_common(1)[0][0])} \\\\\n"
        )
    lines.append(r"\bottomrule" + "\n")
    lines.append(r"\end{tabular}" + "\n")
    lines.append(r"\end{table*}" + "\n\n")

    lines.append(r"\begin{table}[t]" + "\n")
    lines.append(r"\caption{Preliminary complete-suite v0.1 results. Rows are ranked only for runs marked complete by the release manifest.}" + "\n")
    lines.append(r"\label{tab:results}" + "\n")
    lines.append(r"\centering" + "\n")
    lines.append(r"\footnotesize" + "\n")
    lines.append(r"\begin{tabular}{lrrr}" + "\n")
    lines.append(r"\toprule" + "\n")
    lines.append(r"Model & Provider & Verified & Rate \\" + "\n")
    lines.append(r"\midrule" + "\n")
    for row in complete_rows:
        lines.append(
            f"{tex_escape(row['display_name'])} & {tex_escape(row['provider'])} & {row['passed']}/{row['task_count']} & {pct_text(row['passed'], row['task_count'])} \\\\\n"
        )
    lines.append(r"\bottomrule" + "\n")
    lines.append(r"\end{tabular}" + "\n")
    lines.append(r"\end{table}" + "\n\n")

    lines.append(r"\begin{table}[t]" + "\n")
    lines.append(r"\caption{Non-complete v0.1 release artifacts. These artifacts are useful diagnostics but are not ranked with complete-suite runs.}" + "\n")
    lines.append(r"\label{tab:partial-results}" + "\n")
    lines.append(r"\centering" + "\n")
    lines.append(r"\footnotesize" + "\n")
    lines.append(r"\begin{tabular}{llrrr}" + "\n")
    lines.append(r"\toprule" + "\n")
    lines.append(r"Model & Status & Verified & Coverage & Reusable \\" + "\n")
    lines.append(r"\midrule" + "\n")
    for row in noncomplete_rows:
        coverage = f"{row['task_count']}/{summary['task_count']}"
        reusable = f"{row.get('valid_count', row['task_count'])}/{row['task_count']}"
        lines.append(
            f"{tex_escape(row['display_name'])} & {tex_escape(row['status'])} & {row['passed']}/{row['task_count']} & {coverage} & {reusable} \\\\\n"
        )
    lines.append(r"\bottomrule" + "\n")
    lines.append(r"\end{tabular}" + "\n")
    lines.append(r"\end{table}" + "\n\n")

    lines.append(r"\begin{table}[t]" + "\n")
    lines.append(r"\caption{Most represented property classes in v0.1.}" + "\n")
    lines.append(r"\label{tab:properties}" + "\n")
    lines.append(r"\centering" + "\n")
    lines.append(r"\footnotesize" + "\n")
    lines.append(r"\begin{tabular}{lr}" + "\n")
    lines.append(r"\toprule" + "\n")
    lines.append(r"Property class & Tasks \\" + "\n")
    lines.append(r"\midrule" + "\n")
    for prop, count in property_classes.most_common(8):
        lines.append(f"{tex_escape(prop)} & {count} \\\\\n")
    lines.append(r"\bottomrule" + "\n")
    lines.append(r"\end{tabular}" + "\n")
    lines.append(r"\end{table}" + "\n\n")

    lines.append(r"\begin{table}[t]" + "\n")
    lines.append(r"\caption{Proof-family coverage in v0.1.}" + "\n")
    lines.append(r"\label{tab:proof-families}" + "\n")
    lines.append(r"\centering" + "\n")
    lines.append(r"\footnotesize" + "\n")
    lines.append(r"\begin{tabular}{lr}" + "\n")
    lines.append(r"\toprule" + "\n")
    lines.append(r"Proof family & Tasks \\" + "\n")
    lines.append(r"\midrule" + "\n")
    for family, count in proof_families.most_common():
        lines.append(f"{tex_escape(family)} & {count} \\\\\n")
    lines.append(r"\bottomrule" + "\n")
    lines.append(r"\end{tabular}" + "\n")
    lines.append(r"\end{table}" + "\n")

    (out / "metrics.tex").write_text("".join(macros))
    (out / "tables.tex").write_text("".join(lines))
    (out / "snapshot.json").write_text(json.dumps({
        "task_count": len(tasks),
        "case_count": len(cases),
        "family_count": len(set(t["family_id"] for t in tasks)),
        "difficulties": dict(difficulties),
        "complete_results": complete_rows,
        "noncomplete_results": noncomplete_rows,
        "task_set_id": version["task_set_id"],
        "harness_id": version["harness_id"],
        "environment_id": version["environment_id"],
    }, indent=2) + "\n")


if __name__ == "__main__":
    main()
