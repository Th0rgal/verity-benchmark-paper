#!/usr/bin/env python3
import argparse
import csv
import json
from collections import Counter, defaultdict
from pathlib import Path


def tex_escape(value):
    text = str(value)
    replacements = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
        "~": r"\textasciitilde{}",
        "^": r"\textasciicircum{}",
    }
    return "".join(replacements.get(ch, ch) for ch in text)


def pct(part, total):
    return 0 if not total else 100 * part / total


def pct_text(part, total):
    return f"{pct(part, total):.1f}\\%"


def million_text(value):
    return f"{value / 1_000_000:.1f}M"


def tex_number(value):
    return f"{value:.1f}".rstrip("0").rstrip(".")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--benchmark-dir", required=True)
    parser.add_argument("--out-dir", required=True)
    args = parser.parse_args()

    bench = Path(args.benchmark_dir)
    out = Path(args.out_dir)
    out.mkdir(parents=True, exist_ok=True)

    version = json.loads((bench / "benchmark-versions" / "v0.1.json").read_text())
    summary = json.loads((bench / "results" / "summaries" / "v0.1.json").read_text())

    tasks = version["tasks"]
    cases = defaultdict(list)
    for task in tasks:
        cases[task["case_id"]].append(task)

    difficulties = Counter(task["difficulty"] for task in tasks)
    proof_families = Counter(task["proof_family"] for task in tasks)
    property_classes = Counter(task["property_class"] for task in tasks)

    complete_rows = [row for row in summary["models"] if row.get("status") == "complete"]
    complete_rows.sort(key=lambda row: (row["passed"], row["task_count"]), reverse=True)

    noncomplete_rows = [
        row for row in summary["models"] if row.get("status") in {"partial", "invalid"}
    ]
    noncomplete_rows.sort(
        key=lambda row: (
            0 if row.get("status") == "partial" else 1,
            -row["passed"],
            -row["task_count"],
        )
    )

    macros = []
    macros.append("% Generated by scripts/render_tables.py; do not edit manually.\n")
    macros.append(r"\newcommand{\BenchmarkVersion}{0.1}" + "\n")
    macros.append(r"\newcommand{\BenchmarkTaskCount}{" + str(len(tasks)) + "}\n")
    macros.append(r"\newcommand{\BenchmarkCaseCount}{" + str(len(cases)) + "}\n")
    macros.append(r"\newcommand{\BenchmarkFamilyCount}{" + str(len(set(t["family_id"] for t in tasks))) + "}\n")
    macros.append(r"\newcommand{\BenchmarkTaskSetId}{\texttt{" + tex_escape(version["task_set_id"].split(":", 1)[1][:12]) + "}}\n")
    macros.append(r"\newcommand{\BenchmarkHarnessId}{\texttt{" + tex_escape(version["harness_id"].split(":", 1)[1][:12]) + "}}\n")
    macros.append(r"\newcommand{\BenchmarkEnvironmentId}{\texttt{" + tex_escape(version["environment_id"].split(":", 1)[1][:12]) + "}}\n")
    macros.append(r"\newcommand{\BenchmarkGitSha}{\texttt{" + tex_escape(version["git_sha"][:12]) + "}}\n")
    macros.append(r"\newcommand{\BenchmarkEasyTasks}{" + str(difficulties.get("easy", 0)) + "}\n")
    macros.append(r"\newcommand{\BenchmarkMediumTasks}{" + str(difficulties.get("medium", 0)) + "}\n")
    macros.append(r"\newcommand{\BenchmarkHardTasks}{" + str(difficulties.get("hard", 0)) + "}\n")
    macros.append(r"\newcommand{\BenchmarkOtherDifficultyTasks}{" + str(sum(v for k, v in difficulties.items() if k not in {"easy", "medium", "hard"})) + "}\n")

    suite_lines = []
    suite_lines.append("% Generated by scripts/render_tables.py; do not edit manually.\n")
    suite_lines.append(r"\begin{table*}[t]" + "\n")
    suite_lines.append(r"\caption{Benchmark suite composition in v0.1.}" + "\n")
    suite_lines.append(r"\label{tab:suite}" + "\n")
    suite_lines.append(r"\centering" + "\n")
    suite_lines.append(r"\footnotesize" + "\n")
    suite_lines.append(r"\begin{tabular}{lrrl}" + "\n")
    suite_lines.append(r"\toprule" + "\n")
    suite_lines.append(r"Case & Tasks & Hard & Dominant property \\" + "\n")
    suite_lines.append(r"\midrule" + "\n")
    for case, case_tasks in sorted(cases.items()):
        props = Counter(t["property_class"] for t in case_tasks)
        hard = sum(1 for t in case_tasks if t["difficulty"] == "hard")
        suite_lines.append(
            f"{tex_escape(case)} & {len(case_tasks)} & {hard} & {tex_escape(props.most_common(1)[0][0])} \\\\\n"
        )
    suite_lines.append(r"\bottomrule" + "\n")
    suite_lines.append(r"\end{tabular}" + "\n")
    suite_lines.append(r"\end{table*}" + "\n\n")

    suite_lines.append(r"\begin{table}[t]" + "\n")
    suite_lines.append(r"\caption{Most represented property classes in v0.1.}" + "\n")
    suite_lines.append(r"\label{tab:properties}" + "\n")
    suite_lines.append(r"\centering" + "\n")
    suite_lines.append(r"\footnotesize" + "\n")
    suite_lines.append(r"\begin{tabular}{lr}" + "\n")
    suite_lines.append(r"\toprule" + "\n")
    suite_lines.append(r"Property class & Tasks \\" + "\n")
    suite_lines.append(r"\midrule" + "\n")
    for prop, count in property_classes.most_common(8):
        suite_lines.append(f"{tex_escape(prop)} & {count} \\\\\n")
    suite_lines.append(r"\bottomrule" + "\n")
    suite_lines.append(r"\end{tabular}" + "\n")
    suite_lines.append(r"\end{table}" + "\n\n")

    suite_lines.append(r"\begin{table}[t]" + "\n")
    suite_lines.append(r"\caption{Proof-family coverage in v0.1.}" + "\n")
    suite_lines.append(r"\label{tab:proof-families}" + "\n")
    suite_lines.append(r"\centering" + "\n")
    suite_lines.append(r"\footnotesize" + "\n")
    suite_lines.append(r"\begin{tabular}{lr}" + "\n")
    suite_lines.append(r"\toprule" + "\n")
    suite_lines.append(r"Proof family & Tasks \\" + "\n")
    suite_lines.append(r"\midrule" + "\n")
    for family, count in proof_families.most_common():
        suite_lines.append(f"{tex_escape(family)} & {count} \\\\\n")
    suite_lines.append(r"\bottomrule" + "\n")
    suite_lines.append(r"\end{tabular}" + "\n")
    suite_lines.append(r"\end{table}" + "\n")

    result_lines = []
    result_lines.append("% Generated by scripts/render_tables.py; do not edit manually.\n")
    result_lines.append(r"\begin{table}[t]" + "\n")
    result_lines.append(r"\caption{Preliminary complete-suite v0.1 results. Rows are ranked only for runs marked complete by the release manifest.}" + "\n")
    result_lines.append(r"\label{tab:results}" + "\n")
    result_lines.append(r"\centering" + "\n")
    result_lines.append(r"\footnotesize" + "\n")
    result_lines.append(r"\begin{tabular}{rlrr}" + "\n")
    result_lines.append(r"\toprule" + "\n")
    result_lines.append(r"Rank & Model & Verified & Tok./task \\" + "\n")
    result_lines.append(r"\midrule" + "\n")
    for rank, row in enumerate(complete_rows, start=1):
        avg_tokens = row.get("total_tokens", 0) / row["task_count"]
        result_lines.append(
            f"{rank} & {tex_escape(row['display_name'])} & {row['passed']}/{row['task_count']} ({pct_text(row['passed'], row['task_count'])}) & {tex_escape(million_text(avg_tokens))} \\\\\n"
        )
    result_lines.append(r"\bottomrule" + "\n")
    result_lines.append(r"\end{tabular}" + "\n")
    result_lines.append(r"\end{table}" + "\n\n")

    result_lines.append(r"\begin{table}[t]" + "\n")
    result_lines.append(r"\caption{Non-complete v0.1 release artifacts. These artifacts are useful diagnostics but are not ranked with complete-suite runs.}" + "\n")
    result_lines.append(r"\label{tab:partial-results}" + "\n")
    result_lines.append(r"\centering" + "\n")
    result_lines.append(r"\footnotesize" + "\n")
    result_lines.append(r"\begin{tabular}{llrrr}" + "\n")
    result_lines.append(r"\toprule" + "\n")
    result_lines.append(r"Model & Status & Verified & Coverage & Reusable \\" + "\n")
    result_lines.append(r"\midrule" + "\n")
    for row in noncomplete_rows:
        coverage = f"{row['task_count']}/{summary['task_count']}"
        reusable = f"{row.get('valid_count', row['task_count'])}/{row['task_count']}"
        result_lines.append(
            f"{tex_escape(row['display_name'])} & {tex_escape(row['status'])} & {row['passed']}/{row['task_count']} & {coverage} & {reusable} \\\\\n"
        )
    result_lines.append(r"\bottomrule" + "\n")
    result_lines.append(r"\end{tabular}" + "\n")
    result_lines.append(r"\end{table}" + "\n")

    figure_lines = []
    figure_lines.append("% Generated by scripts/render_tables.py; do not edit manually.\n")
    figure_lines.append(r"\begin{figure}[t]" + "\n")
    figure_lines.append(r"\centering" + "\n")
    figure_lines.append(r"\begin{tikzpicture}" + "\n")
    figure_lines.append(r"\begin{axis}[" + "\n")
    figure_lines.append(r"  xbar," + "\n")
    figure_lines.append(r"  xmin=0, xmax=70," + "\n")
    figure_lines.append(r"  width=.86\columnwidth," + "\n")
    figure_lines.append(r"  height=1.7in," + "\n")
    figure_lines.append(r"  ytick=data," + "\n")
    figure_lines.append(r"  y dir=reverse," + "\n")
    figure_lines.append(r"  xlabel={verified tasks}," + "\n")
    figure_lines.append(r"  nodes near coords," + "\n")
    figure_lines.append(r"  nodes near coords align={horizontal}," + "\n")
    figure_lines.append(r"  every node near coord/.append style={font=\scriptsize}," + "\n")
    figure_lines.append(r"  tick label style={font=\scriptsize}," + "\n")
    figure_lines.append(r"  label style={font=\scriptsize}," + "\n")
    figure_lines.append(r"  yticklabels={" + ",".join(tex_escape(row["display_name"]) for row in complete_rows) + r"}," + "\n")
    figure_lines.append(r"  bar width=5pt," + "\n")
    figure_lines.append(r"  axis x line*=bottom," + "\n")
    figure_lines.append(r"  axis y line*=left," + "\n")
    figure_lines.append(r"  enlarge y limits=0.12" + "\n")
    figure_lines.append(r"]" + "\n")
    coords = " ".join(f"({row['passed']},{idx})" for idx, row in enumerate(complete_rows))
    figure_lines.append(r"\addplot[fill=black!65, draw=black!65] coordinates {" + coords + r"};" + "\n")
    figure_lines.append(r"\end{axis}" + "\n")
    figure_lines.append(r"\end{tikzpicture}" + "\n")
    figure_lines.append(r"\caption{Complete-suite v0.1 scores. The best complete run verifies 66 of 135 tasks; no run reaches half of the suite.}" + "\n")
    figure_lines.append(r"\label{fig:leaderboard}" + "\n")
    figure_lines.append(r"\end{figure}" + "\n\n")

    budget_summary = bench / "analysis" / "budget_scaling_minimax_50" / "cascade_summary.csv"
    if budget_summary.exists():
        budget_rows = list(csv.DictReader(budget_summary.open()))
        budget_coords = " ".join(
            f"({float(row['cumulative_total_tokens']) / 1_000_000:.2f},{float(row['solve_rate']) * 100:.1f})"
            for row in budget_rows
        )
        budget_labels = " ".join(
            rf"\node[font=\tiny, anchor=south] at (axis cs:{float(row['cumulative_total_tokens']) / 1_000_000:.2f},{float(row['solve_rate']) * 100:.1f}) {{{row['cumulative_solved']}/50}};"
            for row in budget_rows
            if row["profile"] in {"p01_ultra_tiny", "p05_quick_minus", "p09_high", "p10_ultra_high"}
        )
        figure_lines.append(r"\begin{figure}[t]" + "\n")
        figure_lines.append(r"\centering" + "\n")
        figure_lines.append(r"\begin{tikzpicture}" + "\n")
        figure_lines.append(r"\begin{axis}[" + "\n")
        figure_lines.append(r"  width=.9\columnwidth," + "\n")
        figure_lines.append(r"  height=1.75in," + "\n")
        figure_lines.append(r"  xmin=0, xmax=42," + "\n")
        figure_lines.append(r"  ymin=0, ymax=100," + "\n")
        figure_lines.append(r"  xlabel={cumulative tokens (M)}," + "\n")
        figure_lines.append(r"  ylabel={solve rate (\%)}," + "\n")
        figure_lines.append(r"  tick label style={font=\scriptsize}," + "\n")
        figure_lines.append(r"  label style={font=\scriptsize}," + "\n")
        figure_lines.append(r"  grid=major," + "\n")
        figure_lines.append(r"  mark size=1.4pt" + "\n")
        figure_lines.append(r"]" + "\n")
        figure_lines.append(r"\addplot[black, thick, mark=*] coordinates {" + budget_coords + r"};" + "\n")
        figure_lines.append(budget_labels + "\n")
        figure_lines.append(r"\end{axis}" + "\n")
        figure_lines.append(r"\end{tikzpicture}" + "\n")
        figure_lines.append(r"\caption{MiniMax M3 budget-scaling cascade on a selected 50-task panel. Solved tasks are skipped at later profiles; the run reaches 48/50 before saturating.}" + "\n")
        figure_lines.append(r"\label{fig:minimax-budget}" + "\n")
        figure_lines.append(r"\end{figure}" + "\n")

    (out / "metrics.tex").write_text("".join(macros))
    (out / "suite_tables.tex").write_text("".join(suite_lines))
    (out / "result_tables.tex").write_text("".join(result_lines))
    (out / "result_figures.tex").write_text("".join(figure_lines))
    (out / "tables.tex").write_text("".join(suite_lines + result_lines))
    (out / "snapshot.json").write_text(json.dumps({
        "task_count": len(tasks),
        "case_count": len(cases),
        "family_count": len(set(t["family_id"] for t in tasks)),
        "difficulties": dict(difficulties),
        "complete_results": complete_rows,
        "noncomplete_results": noncomplete_rows,
        "task_set_id": version["task_set_id"],
        "harness_id": version["harness_id"],
        "environment_id": version["environment_id"],
    }, indent=2) + "\n")


if __name__ == "__main__":
    main()
